void bECC_toEPIF(epif_t destination, const elem_t x_value)
{
    uint32_t i = 0;
    bitstr_clear(LB);
    for (i = 0; i < 163; i++)
    {
        //(x_value.testBit(i))
        if (bitstr_getbit(x_value, i))
            // LB = LB.xor (isoTimesAMatrix [i]);
            field_add(LB, LB, isoTimesAMatrix[i]);
    }
    field_add(LB, LB, EPIF_kx);
    bitstr_copy(destination, LB);
}

/* Converts point coordinate to normal representation from EPIF format
 * @param x_value Coordinate in EPIF representation
 * @return Coordinate in standard representation */
void bECC_fromEPIF(epif_t destination, const epif_t source_xepif)
{
    uint32_t i = 0;
    bitstr_clear(LB);
    bitstr_clear(LA);
    field_add(LA, source_xepif, EPIF_kx); // LA = source_xepif.xor(kx);
    for (i = 0; i < 171; i++)
    {
        //(LA.testBit(i))
        if (bitstr_getbit(LA, i))
            // LB = LB.xor(projTimesInvIsoMatrixT [i]);
            field_add(LB, LB, projTimesInvIsoMatrixT[i]);
    }
    bitstr_copy(destination, LB);
}

void bECC_generateChallenge(elem_t x, elem_t r2, elem_t r, elem_t tmp)
{
    // compute R := r*G
    // x_R := [R]x
    bitstr_copy(tmp, Gy); // save Gy
    // store Gx in R(x), because multiplication overwrites first operand
    bitstr_copy(x, Gx);
    point_mult(x, tmp, r);
    // r2 := compute sqrt(b)/x_R
    // 1. Invert  x_R
    field_invert(tmp, x);
    // 2. Mult with sqrtb
    field_mult(r2, tmp, sqrtb);
}

void bECC_precomputeResponse(elem_t resp, elem_t r, elem_t Qx, elem_t Qy, elem_t tmp)
{
    // compute P2 = r*Q
    point_copy(resp, tmp, Qx, Qy);
    point_mult(resp, tmp, r);
}

void bECC_verifyResponse(elem_t result, elem_t r2, elem_t X_P1, elem_t Z_P1)
{
    // compute: result = Z * P2x
    field_mult(result, Z_P1, r2);
    // compare x_P1 == result by subtraction (same as addition for b-field)
    field_add(result, X_P1, result);
}
