/* return the number of the highest one-bit + 1 */
static uint32_t bitstr_sizeinbits(const bitstr_t x)
{
    uint32_t i;
    uint32_t mask;
    for (x += NUMWORDS, i = 32 * NUMWORDS; i > 0 && !*--x; i -= 32)
        ;
    if (i)
        for (mask = 1UL << 31; !(*x & mask); mask >>= 1, i--)
            ;
    return i;
}

/* left-shift by 'count' digits */
static void bitstr_lshift(bitstr_t A, const bitstr_t B, uint32_t count)
{
    uint32_t i, offs = 4 * (count / 32);
    // memmove((void*)A + offs, B, sizeof(bitstr_t) - offs);
    memmove((char *)A + offs, B, sizeof(bitstr_t) - offs);
    memset(A, 0, offs);
    if (count %= 32)
    {
        for (i = NUMWORDS - 1; i > 0; i--)
            A[i] = (A[i] << count) | (A[i - 1] >> (32 - count));
        A[0] <<= count;
    }
}

static uint32_t field_is1(const elem_t x)
{
    uint32_t i;
    if (*x++ != 1)
        return 0;
    for (i = 1; i < NUMWORDS && !*x++; i++)
        ;
    return i == NUMWORDS;
}

static void field_add(elem_t z, const elem_t x, const elem_t y) /* field addition */
{
    uint32_t i;
    for (i = 0; i < NUMWORDS; i++)
        *(z++) = (*(x++) ^ *(y++));
}

static void red_nistb163(elem_t z, uint32_t *x)
{
    uint32_t i;
    uint32_t t;
    for (i = 10; i >= 6; i--)
    {
        t = x[i];
        x[i - 6] = x[i - 6] ^ (t << 29);
        x[i - 5] = x[i - 5] ^ (t << 4) ^ (t << 3) ^ t ^ (t >> 3);
        x[i - 4] = x[i - 4] ^ (t >> 28) ^ (t >> 29);
    }
    t = x[5] >> 3;
    z[0] = x[0] ^ (t << 7) ^ (t << 6) ^ (t << 3) ^ t;
    z[1] = x[1] ^ (t >> 25) ^ (t >> 26);
    z[5] = x[5] & (uint32_t)0x7;
    z[2] = x[2];
    z[3] = x[3];
    z[4] = x[4];
}

static void field_mult(elem_t z, const elem_t x, const elem_t y)
{
    int32_t k;
    uint32_t i, j;
    uint32_t c[11] = {0};
    for (k = 31; k >= 0; k--)
    {
        for (j = 0; j < 6; j++)
        {
            if (word_getbit(x[j], (uint32_t)k) == 1)
            {
                /* c{j} = y + c{j} */
                for (i = 0; i < 6; i++)
                    c[i + j] = c[i + j] ^ y[i];
            }
        }
        if (k != 0)
        {
            for (j = 11 - 1; j > 0; j--)
                c[j] = (c[j] << 1) | (c[j - 1] >> 31);
            c[0] <<= 1;
        }
    }
    red_nistb163(z, c);
}

static void field_square(elem_t z, const elem_t x)
{
    int32_t i;
    uint32_t c[11] = {0};
    for (i = 0; i < 6; i++)
    {
        uint32_t b0 = (x[i] & 0xFF), b1 = ((x[i] >> 8) & 0xFF), b2 = ((x[i] >> 16) & 0xFF), b3 = ((x[i] >> 24) & 0xFF);
        c[2 * i] = ((LUTsq[b1] << 16) | LUTsq[b0]);
        if (i != 5)
            c[2 * i + 1] = ((LUTsq[b3] << 16) | LUTsq[b2]);
    }
    red_nistb163(z, c);
}

/* Implementation of the point addition and point doubling
 * of elliptic curve points represented in Lambda-coordinates.
 * See:
 * Thomaz Oliveira, Julio Lopez, Diego F. Aranha, Francisco Rodroguez-Henroquez:
 * Two is the fastest prime: lambda coordinates for binary elliptic curves.
 * J. Cryptographic Engineering (JCE) 4(1):3-17 (2014)
 * https://eprint.iacr.org/2013/131.pdf
 *
 * We work on an elliptic curve
 * E : y^2+x*y=x^3+a2*x^2+a6
 * defined over a finite field of 2^n elements.
 * Here we assume a2=1.
 * A projective point in Lambda coordinates(X,L,Z) represents
 * an affine point (x,y) as
 * x = X/Z
 * y/x = (L-X)/Z
 * --> y = X(L-X)/Z^2
 */
/* add two points together (X3, L3, Z3) := (X3, L3, Z3) + (X1, L1, Z1) */
static void lambda_add(elem_t X3, elem_t L3, elem_t Z3, const elem_t X1, const elem_t L1, const elem_t Z1)
{
    field_mult(LA, L1, Z3);
    field_mult(LB, L3, Z1);
    field_add(LA, LA, LB);
    field_mult(LB, X1, Z3);
    field_mult(X3, X3, Z1);
    field_add(LC, LB, X3);
    field_square(LC, LC);
    field_mult(LB, LA, LB);
    field_mult(LD, LA, X3);
    field_mult(X3, LA, LC);
    field_mult(LA, X3, Z3);
    field_mult(X3, LB, LD);
    field_add(LB, LD, LC);
    field_add(L3, L1, Z1);
    field_square(LC, LB);
    field_mult(LD, LA, L3);
    field_add(L3, LC, LD);
    field_mult(Z3, LA, Z1);
}

static void lambda_double(elem_t X, elem_t L, elem_t Z)
{
    field_square(LA, Z);
    field_square(LB, L);
    field_mult(L, L, Z);
    field_add(LC, LB, L);
    field_add(LB, LC, LA);
    field_mult(LC, X, Z);
    field_square(X, LB);
    field_mult(Z, LB, LA);
    field_square(LA, LC);
    field_mult(LB, LB, L);
    field_add(LA, LA, X);
    field_add(LA, LA, LB);
    field_add(L, LA, Z);
}

/* point multiplication via double-and-add algorithm */
static void point_mult(elem_t x, elem_t y, const elem_t exp)
{
    static elem_t X, L, Z, z;
    int32_t i;
    /* Convert to Lambda coordinates. */
    bitstr_copy(X, x);
    /* L = y/x+x */
    field_invert(L, x);
    field_mult(L, L, y);
    field_add(L, L, x);
    field_set1(Z);
    bitstr_copy(y, L);
    bitstr_copy(z, Z);
    for (i = bitstr_sizeinbits(exp) - 2; i >= 0; i--)
    {
        lambda_double(X, L, Z);
        if (bitstr_getbit(exp, i))
            lambda_add(X, L, Z, x, y, z);
    }
    /* (X/Z, X(L-X)/Z^2) */
    field_invert(z, Z);
    field_mult(x, X, z);
    field_square(z, z);
    field_add(L, L, X);
    field_mult(X, X, L);
    field_mult(y, z, X);
}

static void field_invert(elem_t z, const elem_t x) /* field inversion */
{
    int32_t i;
    bitstr_copy(LA, x);
    bitstr_copy(LB, ECC_POLY);
    bitstr_clear(LC);
    field_set1(z);
    while (!field_is1(LA))
    {
        i = bitstr_sizeinbits(LA) - bitstr_sizeinbits(LB);
        if (i < 0)
        {
            bitstr_swap(LA, LB);
            bitstr_swap(LC, z);
            i = -i;
        }
        bitstr_lshift(LD, LB, i);
        field_add(LA, LA, LD);
        bitstr_lshift(LD, LC, i);
        field_add(z, z, LD);
    }
}

